let a = Number (prompt('숫자입력'));

switch(a) {
  case 1:
    alert('a는 1입니다.');
    break;
  case 2:
    alert('a는 2입니다.');
    break;
  case 3:
    alert('a는 3입니다.');
    break;
  default:
    alert('a는 1~3이 아닙니다.');
}


